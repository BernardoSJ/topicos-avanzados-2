    public static int pos=0;
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String res=tipo.getSelectedItem().toString();
       if(txtContol.getText().equals("") || txtNip.getText().equals("")){
          JOptionPane.showMessageDialog(null,"Asegurate de que las dos cajas de texto esten llenas","Error",JOptionPane.ERROR_MESSAGE);
       }else{
       switch(res){
           case "Administrador":
               
               for(int i=0;i<Usuarios.registro.administrador.size();i++){
                   if(Usuarios.registro.administrador.get(i).getNoControl().equals(txtContol.getText())){
                       pos=i;
                   }
               }
               if(Usuarios.registro.administrador.get(pos).getNoControl().equals(txtContol.getText())){
                   if(Usuarios.registro.administrador.get(pos).getNip().equals(txtNip.getText())){
                       inicioAdmin n=new inicioAdmin();
                       n.setVisible(true);
                       this.dispose();
                   }else{
                       JOptionPane.showMessageDialog(null,"El NIP es incorrecto","ERROR",JOptionPane.ERROR_MESSAGE);
                   }
               }else{
                   JOptionPane.showMessageDialog(null,"Ese usuario no esta registrado como administrador","ERROR",JOptionPane.ERROR_MESSAGE);
               }
               break;
           case "Alumno":
               
               for(int i=0;i<Usuarios.registro.alumnos.size();i++){
                   if(Usuarios.registro.alumnos.get(i).getNoControl().equals(txtContol.getText())){
                       pos=i;
                   }
               }
               if(Usuarios.registro.alumnos.get(pos).getNoControl().equals(txtContol.getText())){
                   if(Usuarios.registro.alumnos.get(pos).getNip().equals(txtNip.getText())){
                       inicioAlumno n=new inicioAlumno();
                       n.setVisible(true);
                       this.dispose();
                   }else{
                       JOptionPane.showMessageDialog(null,"El NIP es incorrecto","ERROR",JOptionPane.ERROR_MESSAGE);
                   }
               }else{
                   JOptionPane.showMessageDialog(null,"Ese usuario no esta registrado como alumno","ERROR",JOptionPane.ERROR_MESSAGE);
               }
               break;
       }
    }     
    }//GEN-LAST:event_jButton1ActionPerformed

